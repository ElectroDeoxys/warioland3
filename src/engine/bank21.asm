	ld [w2d807], a
.loop
	ld a, [hl] ; red
	ld bc, COLOURFADESTRUCT_LENGTH
	add hl, bc
	ld b, a
	ld a, [hl] ; green
	ld c, a
	rrca
	rrca
	rrca
	and %11100000
	or b
	ld [de], a
	inc de
	ld a, c
	ld bc, COLOURFADESTRUCT_LENGTH
	add hl, bc
	rlca
	rlca
	rlca
	ld c, a
	ld a, [hl] ; blue
	sla c
	rla
	sla c
	rla
	and %1111111
	ld [de], a

	ld a, [w2d807]
	dec a
	ld [w2d807], a
	and a
	ret z
	inc de
	ld bc, COLOURFADESTRUCT_LENGTH
	add hl, bc
	jr .loop

