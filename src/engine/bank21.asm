InitColourFadeStructs:
	ld a, 8 * NUM_PAL_COLORS * 3 
	ld [w2d807], a
	ld hl, wFadePals
.loop
	ld a, [hli] ; COLOURFADESTRUCT_BASE
	ld b, a
	ld e, a
	ld a, [hli] ; COLOURFADESTRUCT_TARGET
	cp b
	jr z, .equal
	ld d, $01 ; increase
	jr nc, .larger
; smaller
	; switch a with b
	ld d, a
	ld a, b
	ld b, d
	ld d, $02 ; decrease
.larger
	sub b
	ld b, a ; difference
	ld a, d
	ld [hli], a ; COLOURFADESTRUCT_SIGN
	ld a, b
	ld [hli], a ; COLOURFADESTRUCT_DIFF
	ld [hli], a ; COLOURFADESTRUCT_ACCUMULATOR
	ld [hl], e ; COLOURFADESTRUCT_CURRENT
	jr .next

.equal
	xor a
	ld [hli], a ; COLOURFADESTRUCT_SIGN
	ld [hli], a ; COLOURFADESTRUCT_DIFF
	ld [hli], a ; COLOURFADESTRUCT_ACCUMULATOR
	ld [hl], e  ; COLOURFADESTRUCT_CURRENT

.next
	ld bc, (COLOURFADESTRUCT_LENGTH + COLOURFADESTRUCT_BASE) - COLOURFADESTRUCT_CURRENT
	add hl, bc
	ld a, [w2d807]
	dec a
	ld [w2d807], a
	ret z
	jr .loop

; writes red/blue/green colour values
; from the pal in de to hl
; in intervals of 8 bytes between them
CopyPalToColourFadeStructs:
	ld a, 8 * NUM_PAL_COLORS
	ld [w2d807], a
.loop
	ld a, [de]
	and %11111
	ld [hl], a ; red
	ld bc, COLOURFADESTRUCT_LENGTH
	add hl, bc
	ld a, [de]
	ld c, a
	inc de
	ld a, [de]
	ld b, a
	ld a, c
	srl b
	rra
	srl b
	rra
	rra
	rra
	rra
	and %11111
	ld [hl], a ; green
	ld a, b
	ld bc, COLOURFADESTRUCT_LENGTH
	add hl, bc
	and %11111
	ld [hl], a ; blue
	inc de
	ld bc, COLOURFADESTRUCT_LENGTH
	add hl, bc

	ld a, [w2d807]
	dec a
	ld [w2d807], a
	and a
	jr nz, .loop
	ret

ApplyPalFadeColours:
