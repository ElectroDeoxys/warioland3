.Func:
	ld a, $02
	ldh [rSVBK], a
	ld a, [wSCY]
	ldh [rSCY], a
	ld a, [wSCX]
	ldh [rSCX], a
	xor a
	ldh [rVBK], a
	ld a, HIGH(wVirtualOAM)
	jp hTransferVirtualOAM
.end

; iterates all treasures in the current page
; and applies the corresponding palette
ApplyPageTreasuresPals:
	xor a
	ld [w2dffe], a
	ld a, [wCollectionPage]
	ld c, a
	inc c
	ld a, -NUM_COLLECTION_CELLS
.loop
	add NUM_COLLECTION_CELLS
	dec c
	jr nz, .loop
	inc a
	ld [w2dfff], a
.loop_treasures
	ld a, [w2dfff]
	call IsTreasureCollected
	jr z, .not_collected
	jr .collected

.next_treasure
	ld hl, w2dffe
	inc [hl]
	ld a, [hli]
	inc [hl] ; w2dfff
	cp NUM_COLLECTION_CELLS
	jr nz, .loop_treasures
	ret

.not_collected
	call .GetAttrmapAddress
	push de
	ld a, $fc
	ld [de], a
	inc a
	ld [hli], a
	inc a
	inc e
	ld [de], a
	inc a
	ld [hl], a
	pop hl
	dec h
	dec h
	dec h
	ld a, $04
	ld [hli], a
	ld [hl], a
	ld de, BG_MAP_WIDTH
	add hl, de
	ld [hld], a
	ld [hl], a
	jr .next_treasure

.collected
	ld a, [w2dfff]
	ld e, a
	ld d, $00
	ld hl, TreasureOBPals
	add hl, de
	ld a, [hl]
	push af
	call .GetAttrmapAddress
	pop af
	dec h
	dec h
	dec h
	ld [hli], a
	ld [hl], a
	dec d
	dec d
	dec d
	ld [de], a
	inc de
	ld [de], a
	jr .next_treasure

; outputs in hl and de the addresses in wTilemap
; for the treasure in Cell w2dffe
; hl = de + BG_MAP_WIDTH
.GetAttrmapAddress:
	ld a, [w2dffe]
	ld d, HIGH(wTilemap) + $1
	cp NUM_COLLECTION_COLS
	jr nc, .got_hi
	ld d, HIGH(wTilemap)
.got_hi
	ld hl, .lo_map_addresses
	ld b, $00
	ld c, a
	add hl, bc
	ld l, [hl]
	ld h, d
	ld e, l
	ld a, BG_MAP_WIDTH
	add l
	ld l, a
	ret

.lo_map_addresses
	db $a3 ; 00
	db $a6 ; 01
	db $a9 ; 02
	db $ac ; 03
	db $af ; 04
	db $03 ; 05
	db $06 ; 06
	db $09 ; 07
	db $0c ; 08
	db $0f ; 09
	db $63 ; 10
	db $66 ; 11
	db $69 ; 12
	db $6c ; 13
	db $6f ; 14
	db $c3 ; 15
	db $c6 ; 16
	db $c9 ; 17
	db $cc ; 18
	db $cf ; 19

