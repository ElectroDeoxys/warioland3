ProcessCollectionLinkState:
	ld hl, wCollectionLinkStateCounter
	inc [hl]
	ld b, h
	ld c, l

	ld a, [wCollectionLinkState]
	jumptable
	dw .AdvanceState
	dw .WaitDelay
	dw .GetLinkedCells
	dw .Return
	dw .Func_9a806
	dw .Return

.AdvanceState:
	ld hl, wCollectionLinkStateCounter
	xor a
	ld [hld], a
	inc [hl] ; wCollectionLinkState
	ret

.WaitDelay:
	ld a, [bc]
	cp $14
	ret c
	jr .AdvanceState

.GetLinkedCells:
	ld hl, CollectionLinkTable
	ld a, [wCollectionPage]
	call GetPointerFromTableHL
	ld a, [wCollectionCell]
	call GetPointerFromTableHL
	ld a, [hl]
	cp $80
	jr z, .AdvanceState
	ld b, 7
	ld de, wCollectionLinkedCells
	call CopyHLToDE
	call .AdvanceState
	jp .AdvanceState

.Return:
	ret

