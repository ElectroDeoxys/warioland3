HandleTreasureCollectionInput:
	ld a, [wJoypadPressed]
	bit B_BUTTON_F, a
	jr z, .asm_9a646

; b btn
	stop_music2
	stop_sfx
	ld a, $08
	ld [wSubState], a
	ret

.asm_9a646
	ld hl, wCollectionPage
	ld a, [wJoypadDown]
	ld b, a
	bit A_BUTTON_F, b
	jr z, .no_a_btn
	ld a, [wCollectionScrollMode]
	and a
	call z, .SetScrollMode
	ld hl, wCollectionPage
	bit D_RIGHT_F, b
	jr nz, .a_right
	bit D_LEFT_F, b
	jr nz, .a_left
	ret

.SetScrollMode:
	ld a, TRUE
	ld [wCollectionScrollMode], a
	ld hl, wSceneObj9State
	ld a, $03
	call SetSceneObjState
	ld hl, wSceneObj10State
	ld a, $04
	call SetSceneObjState
	ret

.a_right
	ld a, $04
	cp [hl]
	jr z, .asm_9a682
	inc [hl]
	jr .asm_9a68f
.asm_9a682
	ld [hl], $00
	jr .asm_9a68f
.a_left
	ld a, [hl]
	and a
	jr z, .asm_9a68d
	dec [hl]
	jr .asm_9a68f
.asm_9a68d
	ld [hl], $04
.asm_9a68f
	play_sfx SFX_0EB
	stop_music2
	ld a, $0c
	ld [wSubState], a
	ret

.no_a_btn
	xor a
	ld [wCollectionScrollMode], a
	ld [wSceneObj9State], a
	ld [wSceneObj10State], a

	ld a, [wJoypadPressed]
	ld b, a
	ld hl, wCollectionCol
	ld a, [hl]
	bit D_RIGHT_F, b
	jr nz, .right
	bit D_LEFT_F, b
	jr nz, .left
	ld hl, wCollectionRow
	ld a, [hl]
	bit D_UP_F, b
	jr nz, .up
	bit D_DOWN_F, b
	jr nz, .down
	call ProcessCollectionLinkState
	ret

.right
	cp NUM_COLLECTION_COLS - 1
	jr z, .page_right
	inc [hl]
	ld a, [wCollectionCell]
	inc a
	jr .got_target_cell
.left
	and a
	jr z, .page_left
	dec [hl]
	ld a, [wCollectionCell]
	dec a
	jr .got_target_cell
.down
	cp NUM_COLLECTION_ROWS - 1
	jr nc, .cant_scroll
	inc [hl]
	ld a, [wCollectionCell]
	add NUM_COLLECTION_COLS
	jr .got_target_cell
.up
	and a
	jr z, .cant_scroll
	dec [hl]
	ld a, [wCollectionCell]
	sub NUM_COLLECTION_COLS
.got_target_cell
	ld [wCollectionCell], a
	play_sfx SFX_0E2

	xor a
	ld [wSceneObj2State], a
	ld [wSceneObj3State], a
	ld [wSceneObj4State], a
	ld [wSceneObj5State], a
	ld [wSceneObj6State], a
	ld [wSceneObj7State], a
	ld [wSceneObj8State], a

	ld de, wCollectionLinkState
	xor a
	ld [de], a ; wCollectionLinkState
	inc e
	ld [de], a ; wCollectionLinkStateCounter
	jr GetCollectionCellCoords

.cant_scroll
	play_sfx SFX_0E5
	ret

.page_right
	ld [hl], $00
	ld a, [wCollectionCell]
	sub NUM_COLLECTION_ROWS
	ld [wCollectionCell], a
	ld hl, wCollectionPage
	inc [hl]
	ld a, [hl]
	cp NUM_COLLECTION_PAGES
	jr nz, .change_page
	ld [hl], $00
	jr .change_page

.page_left
	ld [hl], NUM_COLLECTION_ROWS
	ld a, [wCollectionCell]
	add NUM_COLLECTION_ROWS
	ld [wCollectionCell], a
	ld hl, wCollectionPage
	dec [hl]
	ld a, [hl]
	cp -1
	jr nz, .change_page
	ld [hl], NUM_COLLECTION_PAGES - 1

.change_page
	play_sfx SFX_0EB
	ld a, $0c
	ld [wSubState], a
	ret

